#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int k;

	i = 0;
	k = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
		i++;
	while (k < i)
	{
		s[k] = '\0';
		k++;
	}
}
