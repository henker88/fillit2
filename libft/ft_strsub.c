#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char		*dst;
	size_t		i;

	if (!s)
		return (NULL);
	dst = malloc(sizeof(char) * len + 1);
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len && s[start] != '\0')
	{
		dst[i] = s[start];
		i++;
		start++;
	}
	dst[i] = '\0';
	return (dst);
}
