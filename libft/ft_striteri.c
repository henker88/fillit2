#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int i;
	int k;

	if (s && f)
	{
		i = 0;
		while (s[i] != '\0')
			i++;
		k = 0;
		while (k < i)
		{
			f(k, s + k);
			k++;
		}
	}
}
